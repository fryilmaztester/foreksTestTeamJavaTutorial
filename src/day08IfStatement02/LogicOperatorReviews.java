package day08IfStatement02;

public class LogicOperatorReviews {

    public static void main(String[] args) {

        //int- -Tam sayılar için kullnıyorum

                            //True     &&   //True
                                            //False
        boolean resultOne = (5+2 == 7) && (4+3 ==5) ;// true, false

        //Logical Operator --> Boolean, ya True ya da False dönecek.
        System.out.println(resultOne);

                            //True     veya True
                                            //False
        boolean resultTwo = (5+2 == 7) || (4+3 ==5) ; //true, true

        System.out.println(resultTwo);


    }
}
